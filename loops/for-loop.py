# Create a list of fruit names
fruits = ["apple", "orange", "mango", "banana"]

# Loop through each item in the fruits list
for i in fruits:
    print(f"the fruit is {i}")  # Print the current fruit



# Loop from 0 to 9 using range(10)
for i in range(10):
    print(f"using range from 0 to 10 : {i}")  # Print current number in range
